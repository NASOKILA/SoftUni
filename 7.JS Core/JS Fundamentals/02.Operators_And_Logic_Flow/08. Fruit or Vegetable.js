

function solve(arg) {

    if(arg === "banana"
        || arg === "apple" || arg === "kiwi"
        || arg === "cherry" || arg === "lemon"
        || arg === "grapes" || arg === "peach")
        console.log("fruit");
    else if(arg === "tomato"
        || arg === "cucumber" || arg === "pepper"
        || arg === "onion" || arg === "garlic"
        || arg === "parsley")
        console.log("vegetable");
    else
        console.log("unknown");

}

solve("banana");
solve("cucumber");
solve("pizza");











